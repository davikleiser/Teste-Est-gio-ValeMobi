private void adicionar(){
    String sql = "insert into tb_customer_account(id_customer,cpf_cnpj,nm_customer,is_active,vl_total) values(?,?,?,?,?)";
    
    try {
        
        pst = conexao.prepareStatement(sql);
        pst.setString(1,txtCodCliente.getText());
        pst.setString(2,txtCpf.getText());
        pst.setString(3,txtNmCliente.getText());
        pst.setString(4,cbxEstadoCliente.getSelectedItem().toString());
        pst.setString(5,txtSaldo.getText());
        if(txtCodCliente.getText().isEmpty() || txtNmCliente.getText().isEmpty() || txtCpf.getText().isEmpty() || txtSaldo.getText().isEmpty() || cbxEstadoCliente.getSelectedItem().equals(null) ){
           JOptionPane.showMessageDialog(null, "Preencher todos os campos!"); 
        }else{
       
        int adicionado = pst.executeUpdate();
        
        if(adicionado > 0){
            JOptionPane.showMessageDialog(null, "Cliente adicionado com sucesso!");
            txtCodCliente.setText(null);
            txtCpf.setText(null);
            txtNmCliente.setText(null);
            cbxEstadoCliente.setSelectedItem(null);
            txtSaldo.setText(null);
            
        
        }
        
        }
    } catch (Exception e) {
     
      JOptionPane.showMessageDialog(null, e+": "+"Erro ao cadastrar cliente!");
    
  }
}
